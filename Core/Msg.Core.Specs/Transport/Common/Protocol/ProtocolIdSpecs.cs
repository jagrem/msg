using System;
using FluentAssertions;
using Msg.Core.Transport;
using Msg.Core.Transport.Common.Protocol;
using Msg.Core.Transport.Common.Versioning;
using Xunit;

namespace Msg.Core.Specs.Transport.Common.Protocol
{
    public class ProtocolIdSpecs
    {
        [Fact]
        public void Given_two_instances_of_the_same_protocol_id_When_comparing_Then_it_returns_true()
        {
            //-----------------------------------------------------------------------------------------------------------
            // Arrange
            //-----------------------------------------------------------------------------------------------------------
            var x = new ProtocolId(1);
            var y = new ProtocolId(1);

            //-----------------------------------------------------------------------------------------------------------
            // Act
            //-----------------------------------------------------------------------------------------------------------
            var result = (x == y);
            
            //-----------------------------------------------------------------------------------------------------------
            // Assert
            //-----------------------------------------------------------------------------------------------------------
            result.Should().BeTrue();
        }

        [Fact]
        public void Given_two_different_protocol_ids_When_comparing_Then_it_returns_false()
        {
            //-----------------------------------------------------------------------------------------------------------
            // Arrange
            //-----------------------------------------------------------------------------------------------------------
            var x = new ProtocolId(1);
            var y = new ProtocolId(2);

            //-----------------------------------------------------------------------------------------------------------
            // Act
            //-----------------------------------------------------------------------------------------------------------
            var result = (x == y);
            
            //-----------------------------------------------------------------------------------------------------------
            // Assert
            //-----------------------------------------------------------------------------------------------------------
            result.Should().BeFalse();
        }
    }
}